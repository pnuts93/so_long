#include "so_long.h"

void	get_player(t_vars *v)
{
	int	i;
	int	j;

	i = 0;
	while (i < v->m_xy[1])
	{
		j = 0;
		while (j < v->m_xy[0])
		{
			if (v->map[i][j] == 'P')
			{
				v->p.x = j;
				v->p.y = i;
				v->map[i][j] = '0';
				return ;
			}
			j++;
		}
		i++;
	}
}
